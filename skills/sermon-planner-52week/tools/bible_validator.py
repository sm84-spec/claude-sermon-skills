def normalize_book(name: str) -> Optional[str]:
    name = name.strip()
    if name in BOOK_CHAPTERS:
        return name
    if name in BOOK_ALIASES:
        return BOOK_ALIASES[name]
    # 다른 약어 시도 — 일부 본문은 '복음' 생략
    alt = name.replace("복음", "").strip()
    if alt in BOOK_ALIASES:
        return BOOK_ALIASES[alt]
    return None


# 본문 표기 패턴: "벧전 1:3-5", "시 119편", "사 53장", "롬 8:18-25, 28", "엡 2:8–10"
REF_PATTERN = re.compile(
    r"^(?P<book>[가-힣]+?)\s*"
    r"(?P<chapter>\d+)"
    r"(?:[:장](?P<verse_start>\d+)?(?:[-–~](?P<verse_end>\d+))?)?"
    r"(?:편|장)?$"
)


def parse_reference(ref: str) -> Optional[dict]:
    """본문 인용 문자열을 파싱."""
    ref = ref.strip().replace(" ", "")
    # 1) 책 이름 추출
    m = re.match(r"^([가-힣]+?)(\d+.*)?$", ref)
    if not m:
        return None
    book_part = m.group(1)
    rest = m.group(2) or ""
    book = normalize_book(book_part)
    if not book:
        return None
    # 2) 장·절 파싱
    # 패턴: "1:3-5", "1:3", "1편", "1장", "1:3,5,7" (단순화)
    chap_match = re.match(r"^(\d+)([:장편])?(\d+)?(?:[-–~](\d+))?", rest)
    if not chap_match:
        return None
    chapter = int(chap_match.group(1))
    verse_start = int(chap_match.group(3)) if chap_match.group(3) else None
    verse_end = int(chap_match.group(4)) if chap_match.group(4) else None
    return {
        "book": book,
        "chapter": chapter,
        "verse_start": verse_start,
        "verse_end": verse_end,
    }


def validate_reference(ref: str) -> tuple[bool, str]:
    parsed = parse_reference(ref)
    if not parsed:
        return False, f"본문 형식을 인식할 수 없음: {ref}"
    book = parsed["book"]
    chap = parsed["chapter"]
    vs = parsed["verse_start"]
    ve = parsed["verse_end"]
    chapters = BOOK_CHAPTERS[book]
    if chap < 1 or chap > len(chapters):
        return False, f"{book}은 총 {len(chapters)}장. {chap}장은 정경 범위 밖."
    max_verse = chapters[chap - 1]
    if vs is not None:
        if vs < 1 or vs > max_verse:
            return False, f"{book} {chap}장은 총 {max_verse}절. {vs}절은 범위 밖."
        if ve is not None:
            if ve < vs:
                return False, f"종료 절({ve})이 시작 절({vs})보다 작음."
            if ve > max_verse:
                return False, f"{book} {chap}장은 총 {max_verse}절. {vs}-{ve}절은 종료 범위 밖."
    return True, f"✓ {book} {chap}장 {f'{vs}' if vs else ''}{f'-{ve}' if ve else ''} 정경 범위 내"


def get_chapter_verse_count(book: str, chapter: int) -> Optional[int]:
    norm = normalize_book(book)
    if not norm:
        return None
    chapters = BOOK_CHAPTERS[norm]
    if 1 <= chapter <= len(chapters):
        return chapters[chapter - 1]
    return None


def main() -> int:
    if len(sys.argv) < 2:
        print(__doc__)
        return 1
    if sys.argv[1] == "json":
        print(json.dumps(BOOK_CHAPTERS, ensure_ascii=False))
        return 0
    if sys.argv[1] == "stats":
        total_books = len(BOOK_CHAPTERS)
        total_chapters = sum(len(c) for c in BOOK_CHAPTERS.values())
        total_verses = sum(sum(c) for c in BOOK_CHAPTERS.values())
        print(f"총 {total_books}권 / {total_chapters}장 / {total_verses}절")
        return 0
    ref = " ".join(sys.argv[1:])
    ok, msg = validate_reference(ref)
    print(msg)
    return 0 if ok else 3


if __name__ == "__main__":
    sys.exit(main())
