def build_plan(
    year: int,
    keyword: str,
    keyword_texts_q1: list[str],
    keyword_texts_q2: list[str],
    keyword_texts_q3: list[str],
    keyword_texts_q4: list[str],
    trend_notes: list[dict],
    options: dict | None = None,
) -> dict:
    """52주 완전 plan 생성.

    keyword_texts_qN: 각 분기 키워드 본문 풀. 분기당 5~10개 권장. 각 비절기 주차에 순환 배분.
    """
    cal = compute_all_week_numbers(year)
    weeks = cal["weeks"]
    total_weeks = cal["total_weeks"]

    # 절기 주차 매핑: week -> observance name
    observance_map: dict[int, str] = {}
    for name, info in weeks.items():
        wk = info["week"]
        if isinstance(wk, int):
            if wk in observance_map:
                # 충돌 시 우선순위 — 부활·성탄·성령강림·대강절4 우선
                priority = {"성탄주일": 1, "부활주일": 1, "성령강림주일": 2, "사순절1주": 3,
                            "종려주일": 3, "맥추감사주일": 4, "추수감사주일": 4, "종교개혁주일": 4,
                            "신년주일": 5, "송구영신주일": 5, "대강절4주": 5,
                            "어린이주일": 9, "어버이주일": 9, "광복절통일주일": 9, "추도주일": 9,
                            "대강절1주": 6, "대강절2주": 6, "대강절3주": 6}
                existing = observance_map[wk]
                if priority.get(name, 99) < priority.get(existing, 99):
                    observance_map[wk] = name
            else:
                observance_map[wk] = name

    # Quarter 매핑 (52주 기준)
    def quarter_of(wk: int) -> int:
        if wk <= 13:
            return 1
        if wk <= 26:
            return 2
        if wk <= 39:
            return 3
        return 4

    keyword_pool_by_quarter = {
        1: keyword_texts_q1,
        2: keyword_texts_q2,
        3: keyword_texts_q3,
        4: keyword_texts_q4,
    }

    plan_weeks = []
    keyword_pool_idx = {1: 0, 2: 0, 3: 0, 4: 0}
    hymn_set_idx = 0

    max_week = max(total_weeks, 52)
    for wk in range(1, max_week + 1):
        obs = observance_map.get(wk, "")
        if obs in LITURGY_TEXTS:
            lit = LITURGY_TEXTS[obs]
            week_entry = {
                "week": wk,
                "observance": obs,
                "topic": f"{keyword} 안에서 {obs} 묵상",
                "primary_text": lit["primary"],
                "parallel_texts": lit["parallel"],
                "hymns": [list(h) for h in lit["hymns"]],
            }
        else:
            q = quarter_of(wk)
            pool = keyword_pool_by_quarter[q]
            text = pool[keyword_pool_idx[q] % len(pool)]
            keyword_pool_idx[q] += 1
            hymns = DEFAULT_HYMN_SETS[hymn_set_idx % len(DEFAULT_HYMN_SETS)]
            hymn_set_idx += 1
            week_entry = {
                "week": wk,
                "observance": "—",
                "topic": f"{keyword} Q{q} 흐름",
                "primary_text": text,
                "parallel_texts": [],
                "hymns": [list(h) for h in hymns],
            }
        plan_weeks.append(week_entry)

    return {
        "year": year,
        "keyword": keyword,
        "options": options or {},
        "total_weeks": total_weeks,
        "weeks": plan_weeks,
        "trend_notes": trend_notes,
    }


